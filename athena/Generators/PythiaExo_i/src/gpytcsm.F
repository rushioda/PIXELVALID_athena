      FVOIDP FUNCTION PYTCSM_ADDRESS() 
      IMPLICIT NONE
      INTEGER ITCM
      DOUBLE PRECISION RTCM
      COMMON/PYTCSM/ITCM(0:99),RTCM(0:99)  
      EXTERNAL PYDATA ! All that is required to force loading of BLOCK DATA
      FVOIDP GETADDR
      PYTCSM_ADDRESS = GETADDR(ITCM)
      RETURN
      END
