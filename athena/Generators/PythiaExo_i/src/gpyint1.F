      FVOIDP FUNCTION PYINT1_ADDRESS() 
      IMPLICIT NONE
      INTEGER MINT
      DOUBLE PRECISION VINT
      COMMON/PYINT1/MINT(400),VINT(400)
      EXTERNAL PYDATA ! All that is required to force loading of BLOCK DATA
      FVOIDP GETADDR

      PYINT1_ADDRESS = GETADDR(MINT(1))
      RETURN
      END
