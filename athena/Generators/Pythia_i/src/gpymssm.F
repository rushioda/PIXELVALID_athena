      FVOIDP FUNCTION PYMSSM_ADDRESS() 
      IMPLICIT NONE
      INTEGER ISSM
      DOUBLE PRECISION RSSM
      COMMON /PYMSSM/ ISSM(0:99),RSSM(0:99)
      EXTERNAL PYDATA ! All that is required to force loading of BLOCK DATA
      FVOIDP GETADDR
      PYMSSM_ADDRESS = GETADDR(ISSM)
      RETURN
      END
